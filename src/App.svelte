<script lang="ts">
  import Game from "./lib/Game.svelte";
  import { randStr } from "./utils";
  import { AUTOSAVE_KEY } from "./constants";

  interface Autosave {
    wordCount: number;
    wordLen: number;
    maxGuesses: number;
    seed: string;
    zen: boolean;
    guesses: string[];
  }

  const autosave = JSON.parse(
    localStorage.getItem(AUTOSAVE_KEY) ?? null
  ) as Autosave | null;

  let isStarted = !!autosave;

  let wordCount = autosave?.wordCount ?? 4;
  let wordLen = autosave?.wordLen ?? 5;
  let maxGuesses = autosave?.maxGuesses ?? wordCount + 5;
  let seed = autosave?.seed ?? randStr(5);
  let zen = autosave?.zen ?? false;

  let loadedGuesses: string[] | undefined = autosave?.guesses;
  let guesses: string[];
  let score: number = 0;
  let isGameOver = false;

  $: if (isStarted)
    setTimeout(() => {
      const autosave: Autosave = {
        wordCount,
        wordLen,
        maxGuesses,
        seed,
        zen,
        guesses,
      };
      localStorage.setItem(AUTOSAVE_KEY, JSON.stringify(autosave));
    }, 100);
  $: if (!isStarted) {
    localStorage.removeItem(AUTOSAVE_KEY);
    guesses = [];
  }
</script>

{#if !isStarted}
  <article>
    <h2>How to play</h2>
    <p>
      It's <a href="https://www.nytimes.com/games/wordle/index.html">Wordle</a>
      but with multiple words being guessed simultaneously (like
      <a href="https://zaratustra.itch.io/dordle">Dordle</a>,
      <a href="https://quordle.com">Quordle</a>
      or <a href="https://octordle.com/">Octordle</a>) but also when you guess a
      word, it is replaced with a new one. The game keeps going until you fail
      to guess a word.
    </p>
  </article>
  <form
    on:submit|preventDefault={() => {
      isStarted = true;
    }}
  >
    <div>
      <label for="word-count">Number of words to guess simultaneously:</label>
      <input
        type="number"
        id="word-count"
        bind:value={wordCount}
        on:change={() => {
          maxGuesses = wordCount + 5;
        }}
      />
    </div>
    <div>
      <label for="word-len">Length of words to guess:</label>
      <input type="number" id="word-len" bind:value={wordLen} />
    </div>
    <div>
      <label for="max-guesses">Maximum number of guesses:</label>
      <input type="number" id="max-guesses" bind:value={maxGuesses} />
    </div>
    <div>
      <label for="zen">Zen mode (no losing):</label>
      <input type="checkbox" id="zen" bind:checked={zen} />
    </div>
    <div>
      <label for="seed"
        >Word seed (determines order of words, paste someone else's seed to play
        the same game as them):</label
      >
      <input type="text" id="seed" bind:value={seed} />
    </div>
    <div><input type="submit" value="Play!" /></div>
  </form>
{:else}
  <header class="header">
    {#if zen}
      <span class="zen">Zen mode ðŸ§˜</span>
    {:else}
      Score: {score}
      {#if isGameOver}
        <span class="game-over">Game over!</span>
      {/if}
    {/if}
    <button
      on:click={() => {
        isStarted = false;
        score = 0;
        isGameOver = false;
        seed = randStr(5);
      }}>End game</button
    >
    <span class="seed-box">Seed: <span class="seed">{seed}</span></span>
  </header>
  <Game
    {wordCount}
    {wordLen}
    {maxGuesses}
    {seed}
    {zen}
    {loadedGuesses}
    bind:score
    bind:guesses
    bind:isGameOver
  />
{/if}

